nota = float(input("Digite uma nota:"))

if nota > 10:
    print("você digitou número invalído")

else:
    print("você não digitou maior que 10")

if nota >= 5:
    print("Aprovado")

else:
    print("você nao digitou maior / igual 5")    

if nota >= 3:
    print("Recuperação")
    
else:
    print("você nao digitou maior / igual 3")    


if nota< 0:
    print("você digitou número invalído")

else:
    print("você não digitou número menor que 0 - e está REPROVADO")        